
public class Day_3_TypeCasting {
    //widening type casting
    public static void main(String[] args) {
        //automatically
        int x=10;
        double y= x;
        System.out.println(y);
        
        
        //manuallly
        double a=100;
        System.out.println(a);
        int b= (int)a;
        System.out.println(b);
    }
}
